import turtle

turtle.penup()
for i in range(1, 100, 25):
    turtle.right(90)    # Face south
    turtle.forward(i)   # Move radius
    turtle.right(270)   # Back to start 
    turtle.pendown()    # Put pen down
    turtle.circle(i)    # Draw a circle
    turtle.penup()      # Pen up
    turtle.home()       # Head back to start
    turtle.speed(0)     # Speed up turtle
    
    
